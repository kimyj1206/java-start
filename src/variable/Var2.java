package variable;

public class Var2 {

    public static void main(String[] args) {
        int a; // 변수 선언
        a = 10; // 변수에 값 대입 -> 숫자를 보관할 수 있는 데이터 저장소인 변수 a에 값 10을 저장한다는 의미, 처음으로 값을 대입해서 저장하는 것을 변수 초기화라고 함
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

        // 기존 10 출력 -> 변경 20 출력

        int b; // 변수 선언
        b = 20; // 변수에 값 대입 -> 숫자를 보관할 수 있는 데이터 저장소인 변수 b에 값 20을 저장한다는 의미, 처음으로 값을 대입해서 저장하는 것을 변수 초기화라고 함
        System.out.println(b);
        System.out.println(b);
        System.out.println(b);
    }
}
