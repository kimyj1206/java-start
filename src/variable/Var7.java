package variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100; // 정수 저장
        double b = 10.5; // 실수 저장
        boolean c = true; // true, false 저장
        char d = 'A'; // 문자 저장
        String e = "Hello Java"; // 문자열 저장

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("e = " + e);
    }
}
