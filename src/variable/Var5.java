package variable;

public class Var5 {

    public static void main(String[] args) {
        // 변수 선언과 초기화를 각각
        int a;
        a = 1;
        System.out.println("a = " + a);

        // 변수 선언과 동시에 초기화
        int b = 2;
        System.out.println("b = " + b);

        // 여러 변수 선언과 동시에 초기화
        int c = 3, d = 4;
        System.out.println("c = " + c);
        System.out.println("d = " + d);
    }
}
