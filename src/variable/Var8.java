package variable;

public class Var8 {

    public static void main(String[] args) {
        // 정수
        byte a = 127; // -128 ~ 127
        short b = 32767; // -32,767 ~ 32,767
        int c = 2147483647; // -2,147,483,647 ~ 2,147,483,647
        long d = 9223372036854775807L; // -9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,807

        // 실수
        float e = 10.0f;
        double f = 10.0;
    }
}
